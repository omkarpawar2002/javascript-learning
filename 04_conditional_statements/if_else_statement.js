/*
2.if else statements:-  
        The condition specified after the if keyword is true then the if block will get executed and if the condition specified after the if keyword is not true then it will be skipped and else will get executed.
*/

let age = 3;
if (age >= 18) {
    console.log(`You can easily vote because your age is ${age}`);
} else {
    console.log(`You can not easily vote because your age is ${age}`);
}